function capitalizeWords(str) {
  let result = str.slice(0, 1).toUpperCase();
  for (let i = 1; i < str.length; i++) {
    if (str[i] === " ") {
      result = result + " " + str.slice(i + 1, i + 2).toUpperCase()
      i++
    } else {
      result += str[i]
    }
  }
  return result
}

